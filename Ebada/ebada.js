function switchLanguage(lang) {
    const data = translations[lang];
    const mainHtml = document.getElementById('mainHtml');
    const pageTitle = document.getElementById('pageTitle');
    
    if (!mainHtml || !data) return;

    mainHtml.setAttribute('lang', lang);
    mainHtml.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

    if (pageTitle) pageTitle.innerText = data.pageTitle;

    Object.keys(data).forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = data[id];
            } else if (element.tagName === 'OPTION') {
                element.text = data[id];
            } else {
                element.innerHTML = data[id];
            }
        }
    });

    document.querySelectorAll('.orderBtn').forEach(btn => {
        btn.innerText = data.orderBtn;
    });

    localStorage.setItem('preferredLang', lang);
}

window.addEventListener('storage', (event) => {
    if (event.key === 'preferredLang') {
        switchLanguage(event.newValue);
    }
});

document.addEventListener('DOMContentLoaded', function () {
    const nav = document.querySelector('.navbar');
    const myCarousel = document.querySelector('#testimonialCarousel');
    const contactForm = document.querySelector('form');
    const smoothLinks = document.querySelectorAll('a[href^="#"]');
    const langSwitcher = document.getElementsByClassName('langSwitcher');

    const savedLang = localStorage.getItem('preferredLang') || 'ar';
    switchLanguage(savedLang);

    if (langSwitcher) {
        langSwitcher.addEventListener('click', () => {
            const currentLang = document.getElementById('mainHtml').getAttribute('lang');
            const newLang = currentLang === 'ar' ? 'en' : 'ar';
            switchLanguage(newLang);
        });
    }

    window.addEventListener('scroll', function () {
        if (nav) {
            if (window.scrollY > 100) {
                nav.classList.add('shadow-lg', 'bg-dark');
            } else {
                nav.classList.remove('shadow-lg');
            }
        }
    });

    smoothLinks.forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const targetId = this.getAttribute('href');
            if (targetId.startsWith('#')) {
                e.preventDefault();
                const target = document.querySelector(targetId);
                if (target) {
                    target.scrollIntoView({ behavior: 'smooth' });
                    const offcanvasElement = document.getElementById('offcanvasNavbar');
                    if (offcanvasElement && offcanvasElement.classList.contains('show')) {
                        const bsOffcanvas = bootstrap.Offcanvas.getInstance(offcanvasElement);
                        bsOffcanvas.hide();
                    }
                }
            }
        });
    });

    if (myCarousel) {
        new bootstrap.Carousel(myCarousel, {
            interval: 5000,
            pause: 'hover'
        });
    }

    if (contactForm) {
        contactForm.addEventListener('submit', function (e) {
            e.preventDefault();
            const currentLang = document.getElementById('mainHtml').getAttribute('lang');
            alert(translations[currentLang].alertMsg);
            this.reset();
        });
    }
});
function applyLanguage(lang) {
    const data = translations[lang]; 
    const isEn = lang === 'en';
    document.documentElement.dir = isEn ? 'ltr' : 'rtl';
    document.documentElement.lang = lang;
    
    const langSwitcher = document.getElementById('langSwitcher');
    if (langSwitcher) langSwitcher.textContent = isEn ? 'العربية' : 'English';

    Object.keys(data).forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            const icon = element.querySelector('i');
            if (icon) {
                const iconClass = icon.className;
                element.innerHTML = `${data[id]} <i class="${iconClass}"></i>`;
            } else {
                element.innerHTML = data[id];
            }
        }
    });
    localStorage.setItem('selectedLang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLang') || 'ar';
    applyLanguage(savedLang);

    document.getElementById('langSwitcher')?.addEventListener('click', () => {
        const currentLang = localStorage.getItem('selectedLang') || 'ar';
        applyLanguage(currentLang === 'ar' ? 'en' : 'ar');
    });

    window.addEventListener('storage', (event) => {
        if (event.key === 'selectedLang') {
            applyLanguage(event.newValue);
        }
    });
});